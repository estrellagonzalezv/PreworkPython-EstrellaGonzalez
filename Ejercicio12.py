#CALCULADORA DE ÁREA DE UN RECTÁNGULO: Crea un programa que calcule el area de un rectángulo. El usuario debe ingresar la longitud y el ancho del rectángulo.

longitud = float(input("Ingrese la logitud del rectángulo: "))
ancho = float(input("Ingrese el ancho del rectángulo: "))

area = longitud * ancho

print(f"El área del rectángulo es: {area}")