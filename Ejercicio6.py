#VERIFICACIÓN DE PALÍNDROMO: Crea un programa que verifique si una palabra ingresada por el usuario es un palíndromo (se lee igual de izquiera a derecha que de derecha a izquierda)
palabra = input("Ingrese una palabra: ")
if palabra == palabra [: :-1]:
  print("La palabra es un palíndromo")
else:
  print("La palabra no es un palíndromo")